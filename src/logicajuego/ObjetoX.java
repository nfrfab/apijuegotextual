package logicajuego;

/**
 *
 * @author alumno
 */
public class ObjetoX {
    private String codigoObjetoX;
    private String nombre;
    private Integer valorEntero;
    private Boolean valorBoleano;
    private String valorTexto;

    public ObjetoX() {
    }

    public String getCodigoObjetoX() {
        return codigoObjetoX;
    }

    public void setCodigoObjetoX(String codigoObjetoX) {
        this.codigoObjetoX = codigoObjetoX;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    
    
    public Integer getValorEntero() {
        return valorEntero;
    }

    public void setValorEntero(Integer valorEntero) {
        this.valorEntero = valorEntero;
    }

    public Boolean getValorBoleano() {
        return valorBoleano;
    }

    public void setValorBoleano(Boolean valorBoleano) {
        this.valorBoleano = valorBoleano;
    }

    public String getValorTexto() {
        return valorTexto;
    }

    public void setValorTexto(String valorTexto) {
        this.valorTexto = valorTexto;
    }
    
    
}
